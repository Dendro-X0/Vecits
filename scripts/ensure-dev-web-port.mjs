#!/usr/bin/env node
/**
 * Assign a stable dev port for the Vectis web client (4100–4899).
 * Persists to .dev/web-port and rotates automatically if the saved port is busy.
 * Writes apps/web/.env.local (PORT=).
 */
import { access, mkdir, readFile, writeFile } from "node:fs/promises";
import net from "node:net";
import path from "node:path";
import { fileURLToPath } from "node:url";

const WORKSPACE_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const PORT_FILE = path.join(WORKSPACE_ROOT, ".dev", "web-port");
const ENV_LOCAL = path.join(WORKSPACE_ROOT, "apps", "web", ".env.local");
const PORT_MIN = 4100;
const PORT_MAX = 4899;

async function fileExists(filePath) {
	try {
		await access(filePath);
		return true;
	} catch {
		return false;
	}
}

function isValidPort(value) {
	return Number.isInteger(value) && value >= PORT_MIN && value <= PORT_MAX;
}

async function canBind(port) {
	return new Promise(resolve => {
		const server = net.createServer();
		server.once("error", () => resolve(false));
		server.once("listening", () => {
			server.close(() => resolve(true));
		});
		// Match Next.js dev-server binding behavior (all interfaces / IPv6-aware).
		server.listen(port);
	});
}

async function readPersistedPort() {
	if (!(await fileExists(PORT_FILE))) {
		return null;
	}
	const raw = Number((await readFile(PORT_FILE, "utf8")).trim());
	return isValidPort(raw) ? raw : null;
}

async function pickNewPort() {
	for (let attempt = 0; attempt < 64; attempt += 1) {
		const port = PORT_MIN + Math.floor(Math.random() * (PORT_MAX - PORT_MIN + 1));
		if (await canBind(port)) {
			return port;
		}
	}
	throw new Error(`Could not find a free port in ${PORT_MIN}–${PORT_MAX}`);
}

async function resolvePort() {
	const persisted = await readPersistedPort();
	if (persisted !== null && (await canBind(persisted))) {
		return persisted;
	}
	const port = await pickNewPort();
	await mkdir(path.dirname(PORT_FILE), { recursive: true });
	await writeFile(PORT_FILE, `${port}\n`, "utf8");
	return port;
}

async function writeEnvLocal(port) {
	const body = `# Auto-generated by scripts/ensure-dev-web-port.mjs — do not commit
PORT=${port}
NODE_API_BASE_URL=http://127.0.0.1:7878
NEXT_PUBLIC_NODE_API_BASE_URL=/api/node
`;
	await writeFile(ENV_LOCAL, body, "utf8");
}

async function main() {
	const port = await resolvePort();
	await writeEnvLocal(port);
	const url = `http://127.0.0.1:${port}`;
	console.log(`Vectis web client: ${url}`);
	console.log(`Dev port file: .dev/web-port`);
}

main().catch(error => {
	console.error(error instanceof Error ? error.message : String(error));
	process.exit(1);
});
